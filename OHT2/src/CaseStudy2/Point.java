package CaseStudy2;

public class Point {
	private double x;
	private double y;

	public Point(double x, double y) {
		super();
		this.x = x;
		this.y = y;
	}

	public boolean equals(Point b) {
		if ((this.getX() == b.getX()) && (this.getY() == b.getY())) {
			return true;
		} else
			return false;

	}

	public double getX() {
		return x;
	}

	public void setX(double x) {
		this.x = x;
	}

	public double getY() {
		return y;
	}

	public void setY(double y) {
		this.y = y;
	}

}
